        <h2>Tentang BJ.Habibie</h2>
        <img src="habibie2.jpeg">
        <p><b>Prof. Dr.-Ing. Ir. H. Bacharuddin Jusuf Habibie, FREng. (25 Juni 1936 – 11 September 2019) adalah Presiden Republik Indonesia yang ketiga. Sebelumnya, B.J. Habibie menjabat sebagai Wakil Presiden Republik Indonesia ke-7, menggantikan Try Sutrisno. B. J. Habibie menggantikan Soeharto yang mengundurkan diri dari jabatan presiden pada tanggal 21 Mei 1998.Sebelum memasuki dunia politik, Habibie dikenal luas sebagai seorang profesor dan ilmuwan dalam teknologi aviasi internasional dan satu-satunya presiden Indonesia hingga saat ini yang berlatarbelakang teknokrat.
B.J. Habibie kemudian digantikan oleh Abdurrahman Wahid (Gus Dur) yang terpilih sebagai presiden pada 20 Oktober 1999 oleh MPR hasil Pemilu 1999. Dengan menjabat selama 2 bulan dan 7 hari (sebagai wakil presiden) dan juga selama 1 tahun dan 5 bulan (sebagai presiden), B. J. Habibie merupakan Wakil Presiden dan juga Presiden Indonesia dengan masa jabatan terpendek.
B. J. Habibie merupakan presiden Indonesia pertama yang terlahir di luar Jawa dan berasal dari etnis Gorontalo, Sulawesi dari garis keturunan ayahnya yang berasal dari Kabila, Gorontalo dan etnis Jawa dari ibunya yang berasal dari Yogyakarta.
Saat ini, Pemerintah Provinsi Gorontalo telah membangun Monumen B.J. Habibie di depan pintu gerbang utama Bandar Udara Djalaluddin, di Kabupaten Gorontalo.Selain itu, masyarakat Provinsi Gorontalo pun sempat mengusulkan nama B.J. Habibie digunakan sebagai nama universitas negeri setempat, menggantikan nama Universitas Negeri Gorontalo yang masih digunakan.</p></b>
<b><p>Kelahiran:</b> 25 Juni 1936, Parepare</p>
<b><p>Meninggal:</b> 11 September 2019, RSPAD GATOT Subroto, Jakarta</p>
<b><p>Pendidikan:</b>Habibie belajar tentang keilmuan teknik mesin di Fakultas Teknik Universitas Indonesia Bandung (sekarang Institut Teknologi Bandung) pada tahun 1954. Pada 1955–1965, Habibie melanjutkan studi teknik penerbangan, spesialisasi konstruksi pesawat terbang, di RWTH Aachen, Jerman Barat. Menerima gelar diploma insinyur pada 1960 dan gelar doktor insinyur pada 1965 dengan predikat summa cum laude.</p>
<b><p>Anak:</b> Ilham Akbar Habibie, Thareq Kemal Habibie</p>
<b><p>Masa kepresidenan:</b> 21 Mei 1998 – 20 Oktober 1999</p>
<b><p>Pasangan:</b> Hasri Ainun Besari (m. 1962–2010)</p>
